// Write a function that takes two numbers (a and b) as argument
// Sum a and b
// Return the result

function myFunction(a: number, b: number): number {
  let result = a + b;
  return result;
}

// Test Case
console.log(myFunction(1, 2));
console.log(myFunction(1, 10));
console.log(myFunction(99, 1));
